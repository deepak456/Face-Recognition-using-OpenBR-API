package com.blc.nlp.model;

public class NLP {
	
private String reason;
private String description;
private String oosRecord;
private String ATACode;
private String airportCode;

public String getReason() {
	return reason;
}
public void setReason(String reason) {
	this.reason = reason;
}
public String getDescription() {
	return description;
}
public void setDescription(String description) {
	this.description = description;
}
public String getOosRecord() {
	return oosRecord;
}
public void setOosRecord(String oosRecord) {
	this.oosRecord = oosRecord;
}
public String getATACode() {
	return ATACode;
}
public void setATACode(String aTACode) {
	ATACode = aTACode;
}
public String getAirportCode() {
	return airportCode;
}
public void setAirportCode(String airportCode) {
	this.airportCode = airportCode;
}


}
